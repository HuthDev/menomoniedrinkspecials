	    <p>The Waterfront’s menu has an extensive array of burgers (quarter and half pounders), salads, appetizers, fish, and more. This Menomonie restaurant is known for their stellar Wing Wednesday tradition, shared by other Menomonie bar & grills, as well. They serve up five crispy and coated beer battered wings for $3.50 or 10 for $7.00. They also have Friday Fish Fry, where they serve up delicious baked or beer battered fried fish with a slew of sides. Their beer battered items, like the chicken wings, chicken tenders, cheese curds, and french fries are some of the best in town. They do their frying right, which makes this bar & grill stand out. Their veggie sandwiches are popular, as well as their gouda and jalapeno burger. Also, don’t miss out on their Menomonie drink specials every week, from happy hour everyday to prices cut on pitchers throughout the week.</p>
	   
